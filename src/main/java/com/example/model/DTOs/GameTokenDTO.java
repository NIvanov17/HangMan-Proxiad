package com.example.model.DTOs;

public class GameTokenDTO {

	private String token;

	public GameTokenDTO() {
		super();
	}

	public GameTokenDTO(String token) {
		super();
		this.token = token;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
	
	
}
